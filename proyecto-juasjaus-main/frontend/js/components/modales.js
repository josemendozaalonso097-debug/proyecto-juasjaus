export function abrirInformacion() {
    const modal = document.getElementById('modalInformacion');
    if (modal) {
        modal.style.display = 'block';
        document.body.style.overflow = 'hidden';
    }
}

export function cerrarInformacion() {
    const modal = document.getElementById('modalInformacion');
    if (modal) {
        modal.style.display = 'none';
        document.body.style.overflow = 'auto';
    }
}

export function abrirSeccion(seccion) {
    const data = informacionData[seccion];
    if (!data) return;
    
    const titleElem = document.getElementById('detalleTitle');
    const contentElem = document.getElementById('detalleContenido');
    const modalDetalle = document.getElementById('modalDetalle');
    
    if (titleElem) titleElem.textContent = data.titulo;
    if (contentElem) contentElem.innerHTML = data.contenido;
    
    cerrarInformacion();
    if (modalDetalle) {
        modalDetalle.style.display = 'block';
    }
}

export function cerrarDetalle() {
    const modalDetalle = document.getElementById('modalDetalle');
    if (modalDetalle) {
        modalDetalle.style.display = 'none';
        document.body.style.overflow = 'auto';
    }
}

export function abrirModalOrientacion() {
    const modal = document.getElementById('modalOrientacion');
    if (modal) {
        modal.classList.remove('hidden');
        document.body.style.overflow = 'hidden';
        cambiarTabOrientacion('reporte');
    }
}

export function cerrarModalOrientacion() {
    const modal = document.getElementById('modalOrientacion');
    if (modal) {
        modal.classList.add('hidden');
        document.body.style.overflow = 'auto';
    }
}

export function cambiarTabOrientacion(tab) {
    document.querySelectorAll('.seccion-orientacion').forEach(s => s.classList.add('hidden'));
    const seccion = document.getElementById('seccion-' + tab);
    if (seccion) seccion.classList.remove('hidden');

    document.querySelectorAll('.tab-orientacion').forEach(t => {
        t.classList.remove('border-teal-600', 'text-teal-600');
        t.classList.add('border-transparent', 'text-slate-500');
    });

    const activeTab = document.getElementById('tab-' + tab);
    if (activeTab) {
        activeTab.classList.remove('border-transparent', 'text-slate-500');
        activeTab.classList.add('border-teal-600', 'text-teal-600');
    }
}

export function enviarOrientacion(tipo) {
    let mensajeSuccess = "";

    const elVal = id => {
        const el = document.getElementById(id);
        return el ? el.value.trim() : "";
    };

    if (tipo === 'reporte') {
        const texto = elVal('reporte-texto');
        if (!texto) { showToast("Por favor describe el incidente.", 'warning'); return; }
        mensajeSuccess = "🚨 Reporte enviado con éxito. Orientación revisará el caso a la brevedad.";
        document.getElementById('reporte-texto').value = "";
    } else if (tipo === 'queja') {
        const tipoQueja = elVal('queja-tipo');
        const texto = elVal('queja-texto');
        if (!tipoQueja) { showToast("Por favor selecciona un tipo de queja.", 'warning'); return; }
        mensajeSuccess = "📢 Tu queja sobre \"" + tipoQueja + "\" ha sido recibida. Le daremos seguimiento inmediato.";
        document.getElementById('queja-tipo').value = "";
        document.getElementById('queja-texto').value = "";
    } else if (tipo === 'cita') {
        const motivo = elVal('cita-motivo');
        const horario = elVal('cita-horario');
        mensajeSuccess = "📅 Solicitud de cita enviada (" + horario + "). El psicólogo se pondrá en contacto contigo pronto.";
        document.getElementById('cita-motivo').value = "";
    } else if (tipo === 'buzon') {
        const texto = elVal('buzon-texto');
        if (!texto) { showToast("Por favor escribe tu sugerencia.", 'warning'); return; }
        mensajeSuccess = "📥 ¡Gracias por tu sugerencia! Tu opinión nos ayuda a mejorar el plantel.";
        document.getElementById('buzon-texto').value = "";
    }

    if (mensajeSuccess) {
        showToast(mensajeSuccess, 'success');
        cerrarModalOrientacion();
    }
}

export function inicializarModalesGlobales() {
    const infoBtn = document.getElementById('info-btn');
    if (infoBtn) infoBtn.addEventListener('click', abrirInformacion);

    window.addEventListener('click', function(event) {
        const mInfo = document.getElementById('modalInformacion');
        const mDetalle = document.getElementById('modalDetalle');
        const mOrienta = document.getElementById('modalOrientacion');
        const mHistorial = document.getElementById('modalHistorial');
        
        if (event.target === mInfo) cerrarInformacion();
        if (event.target === mDetalle) cerrarDetalle();
        if (event.target === mOrienta) cerrarModalOrientacion();
        if (event.target === mHistorial && window.cerrarHistorial) window.cerrarHistorial();
    });

    document.addEventListener('keydown', function(event) {
        if (event.key === 'Escape') {
            const mOrienta = document.getElementById('modalOrientacion');
            if (mOrienta && !mOrienta.classList.contains('hidden')) cerrarModalOrientacion();
            
            const mDeuda = document.getElementById('modalDeuda');
            if (mDeuda && !mDeuda.classList.contains('hidden')) mDeuda.classList.add('hidden');
            // Note: History, Pago, and Papeleria also have escape listeners in their modules/setup
        }
    });

}

export function mostrarModalDeuda(pagosPendientes) {
    const modalDeuda = document.getElementById('modalDeuda');
    if (modalDeuda) {
        // Asumiendo $3,000 por pago
        const adeudo = pagosPendientes * 3000;
        
        document.getElementById('modal-deuda-cantidad').textContent = pagosPendientes;
        document.getElementById('modal-deuda-monto').textContent = `$${adeudo.toLocaleString('en-US')}.00 MXN`;
        
        modalDeuda.classList.remove('hidden');
    }
}

